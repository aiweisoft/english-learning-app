function getExamples(type) {
	if (type === 'n') return nounExamples;
	if (type === 'v') return verbExamples;
	if (type === 'a' || type === 'adj') return adjExamples;
	if (type === 'adv') return advExamples;
	return defaultExamples;
}

function buildCategory(id, name, icon, words, startId) {
	const wordEntries = words.map((entry, i) => {
		const [en, zh, type] = entry;
		const wordId = startId + i;
		const phonetic = generatePhonetic(en);
		const examples = getExamples(type);
		const [example, exampleZh] = examples[i % examples.length];
		return {
			id: wordId,
			en: en,
			zh: zh,
			phonetic: phonetic,
			example: example.replace('{word}', en).replace('{zh}', zh),
			exampleZh: exampleZh.replace('{word}', en).replace('{zh}', zh)
		};
	});

	return {
		id: id,
		category: name,
		icon: icon,
		words: wordEntries
	};
}

